#  Buatlah variabel a dengan nilai 10 dan variabel b dengan nilai 3.
# Gunakan operator aritmatika untuk menampilkan hasil dari operasi
# penjumlahan, pengurangan, perkalian, pembagian, dan modulus antara a dan b.

# Contoh Output:

# Penjumlahan: 13
# Pengurangan: 7
# Perkalian: 30
# Pembagian: 3.3333333333333335
# Modulus: 1

a = 10
b = 3

print(f"Penjumlahan: {a + b}")
print(f"Pengurangan: {a - b}")
print(f"Perkalian: {a * b}")
print(f"Pembagian: {a / b}")
print(f"Modulus: {a % b}")