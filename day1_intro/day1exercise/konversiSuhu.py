# Buat program untuk mengonversi suhu dari Celcius ke Fahrenheit. Rumusnya adalah:

# Fahrenheit = (Celcius * 9/5) + 32

suhuCelcius = float(input("Masukkan suhu dalam derajat celcius: "))

suhuFahrenheit = (suhuCelcius * 9/5) + 32

print(f"Suhu tersebut dalam fahreinheit adalah {suhuFahrenheit} derajat")