# Buatlah fungsi yang menerima sebuah list sebagai input dan mengembalikan
# hasil penjumlahan dari semua elemen dalam list tersebut.

emptyList = []

def list():
  total = sum(emptyList)
  return total

while True:
  elemen = int(input("Masukkan Angka: "))
  if elemen == 00 :
    break
  emptyList.append(elemen)

hasil = list()
print(hasil)