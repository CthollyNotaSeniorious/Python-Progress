# Operator Aritmatika

a = 10
b = 3
print("Penjumlahan:", a + b)     # 13
print("Pengurangan:", a - b)     # 7
print("Perkalian:", a * b)       # 30
print("Pembagian:", a / b)       # 3.33
print("Pembagian Bulat:", a // b)# 3
print("Modulus:", a % b)         # 1
print("Pangkat:", a ** b)        # 1000

# Operator Perbandingan

x = 10
y = 5
print(x == y)   # False
print(x != y)   # True
print(x > y)    # True
print(x < y)    # False
print(x >= y)   # True
print(x <= y)   # False

# Opearator Logika

a = True
b = False
print(a and b)    # False
print(a or b)     # True
print(not a)      # False

